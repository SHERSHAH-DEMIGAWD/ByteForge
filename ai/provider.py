def _normalize_topic(topic: str) -> str:
    """Map a free-text topic to a knowledge-base key (e.g. 'Merge Sort' -> 'mergesort')."""
    return re.sub(r"[^a-z0-9]", "", topic.lower())


# ---------------------------------------------------------------------------
# Provider registry + factory (the one place the process picks a provider)
# ---------------------------------------------------------------------------

# Maps the AI_PROVIDER env value to a provider class. New providers register here
# and become selectable by env var alone — no other code changes.
_PROVIDER_REGISTRY: Dict[str, Type[LLMProvider]] = {
    "offline": OfflineProvider,
    # Future, e.g.:
    #   "claude": ClaudeProvider,
    #   "openai": OpenAIProvider,
    #   "gemini": GeminiProvider,
    #   "local":  LocalLLMProvider,
}


def register_provider(key: str, provider_cls: Type[LLMProvider]) -> None:
    """Register a provider implementation under ``key``.

    Exposed so a real provider can be added from a separate module (or a plugin)
    without editing this file. Call this at import time, before the first
    :func:`get_provider` call.
    """
    _PROVIDER_REGISTRY[key.lower()] = provider_cls


@lru_cache(maxsize=1)
def get_provider() -> LLMProvider:
    """Return the process-wide provider selected by the ``AI_PROVIDER`` env var.

    Resolution order:
    1. Read ``AI_PROVIDER`` (default ``"offline"``).
    2. Look it up in :data:`_PROVIDER_REGISTRY`; unknown keys fall back to offline.
    3. If the chosen provider reports it is not available (e.g. a missing API
       key), fall back to the offline provider so requests never hard-fail.

    Cached so the provider is constructed once per process.
    """
    key = os.environ.get("AI_PROVIDER", "offline").strip().lower()
    provider_cls = _PROVIDER_REGISTRY.get(key, OfflineProvider)

    provider = provider_cls()
    if not provider.is_available():
        # Graceful degradation: configured provider can't serve (missing key,
        # SDK not installed, ...). Offline keeps the platform fully usable.
        return OfflineProvider()
    return provider


def describe_provider() -> Dict[str, object]:
    """Return a small, JSON-serializable summary of the active provider.

    Used by the ``GET /ai/health`` endpoint so the frontend can display whether
    a live model is wired up.
    """
    provider = get_provider()
    return {
        "provider": provider.name,
        "is_live": provider.name != "offline",
        "registered": sorted(_PROVIDER_REGISTRY.keys()),
    }
